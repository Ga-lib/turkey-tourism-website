    <script>
        // Scroll animations
        const observer = new IntersectionObserver((entries) => {
            entries.forEach((entry, i) => {
                if (entry.isIntersecting) {
                    setTimeout(() => entry.target.classList.add('visible'), i * 100);
                    observer.unobserve(entry.target);
                }
            });
        }, { threshold: 0.1 });
        document.querySelectorAll('.animate-on-scroll').forEach(el => observer.observe(el));

        // Image slider with dots
        const slider = document.getElementById('slider');
        const slides = slider.getElementsByTagName('img');
        const totalSlides = slides.length;
        let slideIndex = 0;
        let autoSlide;

        const dotsContainer = document.getElementById('sliderDots');
        for (let i = 0; i < totalSlides; i++) {
            const dot = document.createElement('div');
            dot.className = 'slider-dot' + (i === 0 ? ' active' : '');
            dot.addEventListener('click', () => goToSlide(i));
            dotsContainer.appendChild(dot);
        }

        function updateDots() {
            document.querySelectorAll('.slider-dot').forEach((d, i) => {
                d.classList.toggle('active', i === slideIndex);
            });
        }

        function goToSlide(index) {
            slideIndex = index;
            slider.style.transform = `translateX(${-slideIndex * 100}%)`;
            updateDots();
        }

        function nextSlide() {
            slideIndex = (slideIndex + 1) % totalSlides;
            goToSlide(slideIndex);
        }

        autoSlide = setInterval(nextSlide, 3500);

        slider.parentElement.addEventListener('mouseenter', () => clearInterval(autoSlide));
        slider.parentElement.addEventListener('mouseleave', () => { autoSlide = setInterval(nextSlide, 3500); });
    </script>

